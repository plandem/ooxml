package vml_test

import (
	"encoding/xml"
	"fmt"
	"github.com/plandem/ooxml/drawing/vml"
	"github.com/stretchr/testify/require"
	"testing"
)

func TestExtType(t *testing.T) {
	type Entity struct {
		Attribute vml.ExtType `xml:"attribute,attr,omitempty"`
	}

	list := map[vml.ExtType]string{
		vml.ExtType(0):                "",
		vml.ExtTypeEdit:               vml.ExtTypeEdit.String(),
		vml.ExtTypeView:               vml.ExtTypeView.String(),
		vml.ExtTypeBackwardCompatible: vml.ExtTypeBackwardCompatible.String(),
	}

	for k, v := range list {
		t.Run(v, func(tt *testing.T) {
			entity := Entity{Attribute: k}
			encoded, err := xml.Marshal(&entity)

			require.Empty(tt, err)
			if k == 0 {
				require.Equal(tt, `<Entity></Entity>`, string(encoded))
			} else {
				require.Equal(tt, fmt.Sprintf(`<Entity v:attribute="%s"></Entity>`, v), string(encoded))
			}

			var decoded Entity
			err = xml.Unmarshal(encoded, &decoded)
			require.Empty(tt, err)

			require.Equal(tt, entity, decoded)
			require.Equal(tt, v, decoded.Attribute.String())
		})
	}
}
